    public function getSubCategory(Request $request){
        if($request->category_id){
            $category = Category::find($request->category_id);
            $subcategoryies = $category->subCategories;
            $data = "";
            if(count($subcategoryies) > 0){
                foreach ($subcategoryies as $category) {
                $data .= '<option value="' . $category->id . '">' . $category->sub_cat_name . '</option>';
            }
                return ["subCategories" => $data,"message" => "Sub-Category created succesfully!", "code" => 200];
            }else{
                $data .= '<option value="">Selected Category have no any sub category</option>';
                return ["subCategories" => $data,"message" => "Sub-Category created succesfully!", "code" => 200];
            }
        }
        return ["message" => "Something is wrong during get subcategories!", "code" => 100];
    }
